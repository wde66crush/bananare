import { PrismaClient } from "@prisma/client";
const prisma = new PrismaClient();

export const getAllTremrs = async () => {
  const feed = await prisma.post.findMany({
    where: { published: true },
    include: {
      author: {
        select: { name: true },
      },
    },
  });
  return {
    props: { feed },
    revalidate: 10,
  };
};